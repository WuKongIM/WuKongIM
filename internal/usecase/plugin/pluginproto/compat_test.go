package pluginproto

import (
	"bytes"
	"testing"

	"google.golang.org/protobuf/proto"
)

func TestPluginProtoCompatibility(t *testing.T) {
	tests := []struct {
		name string
		msg  interface {
			Marshal() ([]byte, error)
		}
		empty interface {
			Unmarshal([]byte) error
		}
		want []byte
	}{
		{
			name: "PluginInfo",
			msg: &PluginInfo{
				No:               "demo",
				Name:             "Demo Plugin",
				Methods:          []string{"Send", "Receive"},
				Version:          "1.2.3",
				Priority:         9,
				PersistAfterSync: true,
				ReplySync:        true,
				ConfigTemplate: &ConfigTemplate{Fields: []*Field{{
					Name:  "mode",
					Type:  "string",
					Label: "Mode",
				}}},
			},
			empty: &PluginInfo{},
			want:  []byte{0xa, 0x4, 0x64, 0x65, 0x6d, 0x6f, 0x12, 0xb, 0x44, 0x65, 0x6d, 0x6f, 0x20, 0x50, 0x6c, 0x75, 0x67, 0x69, 0x6e, 0x1a, 0x4, 0x53, 0x65, 0x6e, 0x64, 0x1a, 0x7, 0x52, 0x65, 0x63, 0x65, 0x69, 0x76, 0x65, 0x22, 0x5, 0x31, 0x2e, 0x32, 0x2e, 0x33, 0x28, 0x9, 0x30, 0x1, 0x38, 0x1, 0x42, 0x16, 0xa, 0x14, 0xa, 0x4, 0x6d, 0x6f, 0x64, 0x65, 0x12, 0x6, 0x73, 0x74, 0x72, 0x69, 0x6e, 0x67, 0x1a, 0x4, 0x4d, 0x6f, 0x64, 0x65},
		},
		{
			name: "SendPacket",
			msg: &SendPacket{
				FromUid:     "u1",
				ChannelId:   "c1",
				ChannelType: 2,
				Payload:     []byte("hello"),
				Reason:      1,
				Conn: &Conn{
					Uid:         "u1",
					ConnId:      42,
					DeviceId:    "dev",
					DeviceFlag:  1,
					DeviceLevel: 2,
				},
			},
			empty: &SendPacket{},
			want:  []byte{0xa, 0x2, 0x75, 0x31, 0x12, 0x2, 0x63, 0x31, 0x18, 0x2, 0x22, 0x5, 0x68, 0x65, 0x6c, 0x6c, 0x6f, 0x28, 0x1, 0x32, 0xf, 0xa, 0x2, 0x75, 0x31, 0x10, 0x2a, 0x1a, 0x3, 0x64, 0x65, 0x76, 0x20, 0x1, 0x28, 0x2},
		},
		{
			name: "StartupResp",
			msg: &StartupResp{
				NodeId:     1001,
				Success:    true,
				SandboxDir: "/tmp/sandbox",
				Config:     []byte(`{"enabled":true}`),
			},
			empty: &StartupResp{},
			want:  []byte{0x8, 0xe9, 0x7, 0x10, 0x1, 0x22, 0xc, 0x2f, 0x74, 0x6d, 0x70, 0x2f, 0x73, 0x61, 0x6e, 0x64, 0x62, 0x6f, 0x78, 0x2a, 0x10, 0x7b, 0x22, 0x65, 0x6e, 0x61, 0x62, 0x6c, 0x65, 0x64, 0x22, 0x3a, 0x74, 0x72, 0x75, 0x65, 0x7d},
		},
		{
			name: "ForwardHttpReq",
			msg: &ForwardHttpReq{
				PluginNo: "demo",
				ToNodeId: 7,
				Request: &HttpRequest{
					Method:  "POST",
					Path:    "/plugins/demo/ping",
					Headers: map[string]string{"x-token": "abc"},
					Query:   map[string]string{"debug": "1"},
					Body:    []byte("body"),
				},
			},
			empty: &ForwardHttpReq{},
			want:  []byte{0xa, 0x4, 0x64, 0x65, 0x6d, 0x6f, 0x10, 0x7, 0x1a, 0x3c, 0xa, 0x4, 0x50, 0x4f, 0x53, 0x54, 0x12, 0x12, 0x2f, 0x70, 0x6c, 0x75, 0x67, 0x69, 0x6e, 0x73, 0x2f, 0x64, 0x65, 0x6d, 0x6f, 0x2f, 0x70, 0x69, 0x6e, 0x67, 0x1a, 0xe, 0xa, 0x7, 0x78, 0x2d, 0x74, 0x6f, 0x6b, 0x65, 0x6e, 0x12, 0x3, 0x61, 0x62, 0x63, 0x22, 0xa, 0xa, 0x5, 0x64, 0x65, 0x62, 0x75, 0x67, 0x12, 0x1, 0x31, 0x2a, 0x4, 0x62, 0x6f, 0x64, 0x79},
		},
		{
			name:  "StreamOpenResp",
			msg:   &StreamOpenResp{StreamNo: "stream-1"},
			empty: &StreamOpenResp{},
			want:  []byte{0xa, 0x8, 0x73, 0x74, 0x72, 0x65, 0x61, 0x6d, 0x2d, 0x31},
		},
		{
			name: "Stream",
			msg: &Stream{
				Header:      &Header{NoPersist: true, RedDot: true, SyncOnce: true},
				ClientMsgNo: "cmn-open",
				FromUid:     "u1",
				ChannelId:   "c1",
				ChannelType: 2,
				Payload:     []byte("start"),
			},
			empty: &Stream{},
			want:  []byte{0xa, 0x6, 0x8, 0x1, 0x10, 0x1, 0x18, 0x1, 0x12, 0x8, 0x63, 0x6d, 0x6e, 0x2d, 0x6f, 0x70, 0x65, 0x6e, 0x1a, 0x2, 0x75, 0x31, 0x22, 0x2, 0x63, 0x31, 0x28, 0x2, 0x32, 0x5, 0x73, 0x74, 0x61, 0x72, 0x74},
		},
		{
			name:  "StreamCloseReq",
			msg:   &StreamCloseReq{StreamNo: "stream-1", ChannelId: "c1", ChannelType: 2},
			empty: &StreamCloseReq{},
			want:  []byte{0xa, 0x8, 0x73, 0x74, 0x72, 0x65, 0x61, 0x6d, 0x2d, 0x31, 0x12, 0x2, 0x63, 0x31, 0x18, 0x2},
		},
		{
			name: "StreamWriteReq",
			msg: &StreamWriteReq{
				Header:      &Header{NoPersist: true, RedDot: true, SyncOnce: true},
				StreamNo:    "stream-1",
				ClientMsgNo: "cmn-1",
				FromUid:     "u1",
				ChannelId:   "c1",
				ChannelType: 2,
				Payload:     []byte("chunk"),
			},
			empty: &StreamWriteReq{},
			want:  []byte{0xa, 0x6, 0x8, 0x1, 0x10, 0x1, 0x18, 0x1, 0x12, 0x8, 0x73, 0x74, 0x72, 0x65, 0x61, 0x6d, 0x2d, 0x31, 0x1a, 0x5, 0x63, 0x6d, 0x6e, 0x2d, 0x31, 0x22, 0x2, 0x75, 0x31, 0x2a, 0x2, 0x63, 0x31, 0x30, 0x2, 0x3a, 0x5, 0x63, 0x68, 0x75, 0x6e, 0x6b},
		},
		{
			name:  "StreamWriteResp",
			msg:   &StreamWriteResp{MessageId: 987654321, ClientMsgNo: "cmn-write"},
			empty: &StreamWriteResp{},
			want:  []byte{0x8, 0xb1, 0xd1, 0xf9, 0xd6, 0x3, 0x12, 0x9, 0x63, 0x6d, 0x6e, 0x2d, 0x77, 0x72, 0x69, 0x74, 0x65},
		},
		{
			name: "SendReq",
			msg: &SendReq{
				Header:      &Header{NoPersist: true, SyncOnce: true},
				ClientMsgNo: "cmn-2",
				FromUid:     "u2",
				ChannelId:   "c2",
				ChannelType: 3,
				Payload:     []byte("send"),
			},
			empty: &SendReq{},
			want:  []byte{0xa, 0x4, 0x8, 0x1, 0x18, 0x1, 0x12, 0x5, 0x63, 0x6d, 0x6e, 0x2d, 0x32, 0x1a, 0x2, 0x75, 0x32, 0x22, 0x2, 0x63, 0x32, 0x28, 0x3, 0x32, 0x4, 0x73, 0x65, 0x6e, 0x64},
		},
		{
			name:  "SendResp",
			msg:   &SendResp{MessageId: 123456789},
			empty: &SendResp{},
			want:  []byte{0x8, 0x95, 0x9a, 0xef, 0x3a},
		},
		{
			name: "ChannelMessageBatchReq",
			msg: &ChannelMessageBatchReq{ChannelMessageReqs: []*ChannelMessageReq{{
				ChannelId:       "c1",
				ChannelType:     2,
				StartMessageSeq: 10,
				Limit:           50,
			}}},
			empty: &ChannelMessageBatchReq{},
			want:  []byte{0xa, 0xa, 0xa, 0x2, 0x63, 0x31, 0x10, 0x2, 0x18, 0xa, 0x20, 0x32},
		},
		{
			name: "ChannelMessageBatchResp",
			msg: &ChannelMessageBatchResp{ChannelMessageResps: []*ChannelMessageResp{{
				ChannelId:       "c1",
				ChannelType:     2,
				StartMessageSeq: 10,
				Limit:           50,
				Messages: []*Message{{
					MessageId:   99,
					MessageSeq:  10,
					ClientMsgNo: "cmn",
					StreamNo:    "stream",
					StreamId:    3,
					Timestamp:   1700000000,
					From:        "u1",
					ChannelId:   "c1",
					ChannelType: 2,
					Topic:       "topic",
					Payload:     []byte("payload"),
				}},
			}}},
			empty: &ChannelMessageBatchResp{},
			want:  []byte{0xa, 0x3f, 0xa, 0x2, 0x63, 0x31, 0x10, 0x2, 0x18, 0xa, 0x20, 0x32, 0x2a, 0x33, 0x8, 0x63, 0x10, 0xa, 0x1a, 0x3, 0x63, 0x6d, 0x6e, 0x22, 0x6, 0x73, 0x74, 0x72, 0x65, 0x61, 0x6d, 0x28, 0x3, 0x30, 0x80, 0xe2, 0xcf, 0xaa, 0x6, 0x3a, 0x2, 0x75, 0x31, 0x42, 0x2, 0x63, 0x31, 0x48, 0x2, 0x52, 0x5, 0x74, 0x6f, 0x70, 0x69, 0x63, 0x5a, 0x7, 0x70, 0x61, 0x79, 0x6c, 0x6f, 0x61, 0x64},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got, err := tt.msg.Marshal()
			if err != nil {
				t.Fatalf("Marshal() error = %v", err)
			}
			if !bytes.Equal(got, tt.want) {
				t.Fatalf("Marshal() = %#v, want %#v", got, tt.want)
			}

			if err := tt.empty.Unmarshal(tt.want); err != nil {
				t.Fatalf("Unmarshal() error = %v", err)
			}
			if !proto.Equal(tt.empty.(proto.Message), tt.msg.(proto.Message)) {
				t.Fatalf("Unmarshal() = %#v, want %#v", tt.empty, tt.msg)
			}
		})
	}
}
